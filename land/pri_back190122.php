<p>수집된 회원의 개인정보는 이용자의 동의없이 서비스 목적 이외에 이용하거나 제3자에게 제공할 수 없는 것을 원칙으로 합니다. 다만 다음의 이용목적에는 예외로 합니다.</p>

<p>개인정보의 이용목적은 다음 각 호와 같으며, 회사는 비즈니스 파트너와의 제휴를 통해 회원에게 제공되는 서비스 질을 향상 시키기 위해서 당사 자회사 및 제휴 협력관계사가 운영하는 콜센터를 통한 보험,</p>

<p>신용카드 발급 등의 텔레마케팅 서비스, 통계작성 또는 시장조사, 등을 위하여 필요한 최소한의 정보(성명, 아이핀인증값(CI), 생년월일, 주소, 이메일 주소, 휴대폰 번호)를 타사에게 제공하거나 공유할 수 있습니다.</p>

<p>이때 이용자의 약관 동의는 아래 개인정보 활용에 동의한 것으로 간주 합니다.</p>

<p>가. 서비스 제공 (텔레마케팅 서비스를 포함 ) 과 회원관리</p>

<p>나. 고객의 이메일 및 SMS로 이벤트 당첨 및 광고, 이벤트 정보 전송</p>

<p>다. 회사와의 업무제휴로 인한 회원의 정보 제공 및 활용</p>

<p>라. 회원 전체 또는 일부의 개인정보에 관한 통계자료를 작성, 개인의 맞춤형 서비스, 학술적인 연구,시장조사를 위하여 필요한 경우로서 특정 개인을 식별할 수 없는 형태로 제공하는 경우 </p><br>


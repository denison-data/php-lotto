<p>회사는 회원가입, 원활한 고객상담, 각종 서비스의 제공을 위해 아래와 같은 개인정보를 수집하고 있습니다.</p>

<p>- 성명, 휴대폰 번호</p>

<p>또한 서비스 이용과정이나 사업 처리 과정에서 아래와 같은 정보들이 생성되어 수집될 수 있습니다.</p>

<p>- 이용자의 브라우저 종류 및 OS, 서비스 이용 기록</p>

